"""
Pipeline Report Generator — Satellite Defence
================================================
Generates a Markdown summary report after each pipeline run.
"""

import json
import logging
import subprocess
from datetime import datetime
from pathlib import Path

logger = logging.getLogger(__name__)


def _get_git_info() -> dict:
    """Get current git commit info."""
    info = {}
    try:
        info["commit"] = subprocess.check_output(
            ["git", "rev-parse", "--short", "HEAD"], stderr=subprocess.DEVNULL
        ).decode().strip()
        info["branch"] = subprocess.check_output(
            ["git", "rev-parse", "--abbrev-ref", "HEAD"], stderr=subprocess.DEVNULL
        ).decode().strip()
    except Exception:
        info["commit"] = "unknown"
        info["branch"] = "unknown"
    return info


def generate_report(results: dict, cfg: dict, output_dir: str = "./eval_results") -> str:
    """
    Generate a Markdown pipeline report.

    Args:
        results: Pipeline run results dict from pipeline.py.
        cfg: Pipeline configuration dict.
        output_dir: Where to save the report.

    Returns:
        Path to the generated report file.
    """
    out = Path(output_dir)
    out.mkdir(parents=True, exist_ok=True)

    git = _get_git_info()
    run_id = results.get("run_id", "unknown")
    lines = []

    # Header
    lines.append("# 🛰️ Satellite Defence — Pipeline Report")
    lines.append("")
    lines.append(f"**Run ID:** `{run_id}`  ")
    lines.append(f"**Date:** {results.get('started_at', 'N/A')}  ")
    lines.append(f"**Git:** `{git['branch']}@{git['commit']}`  ")
    lines.append(f"**Duration:** {results.get('total_duration_s', 0):.1f}s  ")
    lines.append(f"**Status:** {'✅ SUCCESS' if results.get('success') else '❌ FAILED'}  ")
    lines.append(f"**Dry Run:** {'Yes' if results.get('dry_run') else 'No'}  ")
    lines.append("")
    lines.append("---")
    lines.append("")

    # Stage Summary Table
    lines.append("## Stage Summary")
    lines.append("")
    lines.append("| Stage | Status | Duration |")
    lines.append("|-------|--------|----------|")
    for stage_name, stage_data in results.get("stages", {}).items():
        status = stage_data.get("status", "?")
        icon = {"success": "✅", "dry_run": "🔵", "skipped": "⏭️", "error": "❌"}.get(status, "❓")
        dur = stage_data.get("duration_s", 0)
        lines.append(f"| {stage_name} | {icon} {status} | {dur:.1f}s |")
    lines.append("")

    # Metrics (if evaluation ran)
    eval_data = results.get("stages", {}).get("evaluate", {})
    metrics = eval_data.get("metrics", {})
    if metrics:
        lines.append("## Evaluation Metrics")
        lines.append("")
        lines.append(f"- **mAP@0.5:** {metrics.get('map50', 0):.4f}")
        lines.append(f"- **mAP@0.5:0.95:** {metrics.get('map50_95', 0):.4f}")
        lines.append("")
        lines.append("### Per-Class Breakdown")
        lines.append("")
        lines.append("| Class | AP@50 | Precision | Recall | F1 | FPR |")
        lines.append("|-------|-------|-----------|--------|-----|-----|")
        for cls_name, m in metrics.get("per_class", {}).items():
            lines.append(
                f"| {cls_name} | {m.get('ap50',0):.3f} | "
                f"{m.get('precision',0):.3f} | {m.get('recall',0):.3f} | "
                f"{m.get('f1',0):.3f} | {m.get('fpr',0):.3f} |"
            )
        lines.append("")

    # Quality Gate
    gate_passed = eval_data.get("quality_gate_passed", None)
    if gate_passed is not None:
        lines.append("## Quality Gate")
        lines.append("")
        lines.append(f"**Result:** {'✅ PASSED' if gate_passed else '❌ FAILED'}")
        lines.append("")
        warnings = eval_data.get("quality_gate_warnings", [])
        if warnings:
            for w in warnings:
                lines.append(f"- ⚠️ {w}")
            lines.append("")

    # Inference summary
    infer_data = results.get("stages", {}).get("infer", {})
    if infer_data.get("status") == "success":
        lines.append("## Inference")
        lines.append("")
        lines.append(f"- Images processed: {infer_data.get('images', 0)}")
        lines.append(f"- Total detections: {infer_data.get('detections', 0)}")
        lines.append("")

    # Config summary
    lines.append("## Configuration")
    lines.append("")
    lines.append("```yaml")
    train_cfg = cfg.get("training", {})
    lines.append(f"weights: {train_cfg.get('weights', 'N/A')}")
    lines.append(f"epochs: {train_cfg.get('epochs', 'N/A')}")
    lines.append(f"batch: {train_cfg.get('batch', 'N/A')}")
    lines.append(f"imgsz: {train_cfg.get('imgsz', 'N/A')}")
    lines.append(f"device: {train_cfg.get('device', 'N/A')}")
    lines.append("```")
    lines.append("")
    lines.append("---")
    lines.append(f"*Generated by satellite-defence pipeline v2*")

    report_text = "\n".join(lines)
    report_path = out / f"pipeline_report_{run_id}.md"
    with open(report_path, "w", encoding="utf-8") as f:
        f.write(report_text)

    # Also write a latest symlink/copy
    latest = out / "pipeline_report.md"
    with open(latest, "w", encoding="utf-8") as f:
        f.write(report_text)

    logger.info("Report saved to %s", report_path)
    return str(report_path)
